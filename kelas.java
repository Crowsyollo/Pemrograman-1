    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().trim().isEmpty() || 
        jTextField2.getText().trim().isEmpty() || 
        jTextField3.getText().trim().isEmpty()) {
        
        JOptionPane.showMessageDialog(null, "Semua data kelas wajib diisi! Jangan dikosongkan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    // 2. PROSES INSERT KE DATABASE MYSQL
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO kelas (nama_kelas, Jadwal, Pengajar) VALUES (?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, jTextField1.getText()); // Mengambil Nama Kelas
        pst.setString(2, jTextField2.getText()); // Mengambil Jadwal
        pst.setString(3, jTextField3.getText()); // Mengambil Pengajar
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Kelas Berhasil Disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE); 
        
        // Membersihkan kotak form setelah sukses simpan
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.requestFocus();
        
        tampilData(); // Refresh tabel aplikasi secara otomatis
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Proses Simpan Gagal: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE); 
    }
    }//GEN-LAST:event_jButton1ActionPerformed

