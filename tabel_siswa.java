    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel(); // Buat model tabel baru [cite: 488]
        model.addColumn("ID Siswa"); // [cite: 490]
        model.addColumn("Nama Siswa"); // [cite: 492]
        model.addColumn("Jenis Kelamin"); // [cite: 494]
        model.addColumn("Tanggal Lahir"); // [cite: 496]
        model.addColumn("Alamat"); // [cite: 498]

        try {
            Connection conn = Koneksi.getConnection(); // Panggil koneksi dari class Koneksi [cite: 504]
            String sql = "SELECT * FROM siswa"; // Pastikan nama tabel di phpMyAdmin adalah 'siswa' [cite: 505]
            Statement st = conn.createStatement(); // [cite: 508]
            ResultSet rs = st.executeQuery(sql); // [cite: 511]
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_siswa"), // Sesuaikan dengan nama kolom database-mu [cite: 519]
                    rs.getString("nama_siswa"), // [cite: 521]
                    rs.getString("jenis_kelamin"), // [cite: 523]
                    rs.getString("tanggal_lahir"), // [cite: 525]
                    rs.getString("alamat_siswa") // [cite: 527]
                });
            }
            jTable3.setModel(model); // Masukkan data ke komponen JTable jFrame [cite: 534]
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menampilkan Data: " + e.getMessage()); // [cite: 541]
        }
    }

