    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // 1. VALIDASI INPUT KOSONG (Agar data kosong tidak disimpan)
    if (jTextField1.getText().trim().isEmpty() || 
        jTextField2.getText().trim().isEmpty() || 
        jTextField3.getText().trim().isEmpty() || 
        jTextArea1.getText().trim().isEmpty()) {
        
        JOptionPane.showMessageDialog(null, "Semua data wajib diisi! Jangan dikosongkan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    // 2. PROSES SIMPAN DATA KE MYSQL
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO orang_tua (nama_ayah, nama_ibu, no_telepon, alamat) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, jTextField1.getText());
        pst.setString(2, jTextField2.getText());
        pst.setString(3, jTextField3.getText());
        pst.setString(4, jTextArea1.getText());
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Orang Tua Berhasil Disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE); 
        
        // Membersihkan form setelah sukses simpan
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextArea1.setText("");
        jTextField1.requestFocus();
        
        tampilData(); // Memuat ulang tabel aplikasi otomatis
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Proses Gagal: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE); 
    }
    }//GEN-LAST:event_jButton1ActionPerformed

