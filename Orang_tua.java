    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Orang Tua");
        model.addColumn("Nama Ayah");
        model.addColumn("Nama Ibu");
        model.addColumn("No telepon");
        model.addColumn("Alamat");

        try {
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM orang_tua"; 
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_orang_tua"), // Sesuai kolom di phpMyAdmin kamu
                    rs.getString("nama_ayah"),
                    rs.getString("nama_ibu"),
                    rs.getString("no_telepon"),       // Sesuai kolom di phpMyAdmin kamu
                    rs.getString("alamat")
                });
            }
            // Cek nama variabel JTable di desainmu, ganti jTable1 jika berbeda (misal jTable2/jTable3)
            jTable1.setModel(model); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menampilkan Data: " + e.getMessage());
        }
    }
    
    
